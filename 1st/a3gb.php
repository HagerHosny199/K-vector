<h3 dir="rtl" lang="ar" style="padding-right:20px;">زراعة الأفكار</h3>
<h4 dir="rtl" lang="ar" style="padding-right:20px;">(1901 م – 1967 م)Dr. Ewen Cameron</h4>
<p  dir="rtl" lang="ar" style="text-indent:30px;">
(Ewen Cameron) طبيب نفسي، اسكتلندي الأصل، اعتقد أن لديه علاج لمرض (سكيزوفرنيا أو شيزوفرنيا) من خلال برمجة المخ وإقحام أفكار جديدة إليه. كانت طريقته تدعى "التوجيه النفسي" وفيه يرتدي المريض سماعات يبث من خلالها رسائل صوتية تتكرر باستمرار، ويظل يستمع إليها المريض لعدة أيام وربما لعدة أسابيع على غرار "الناس جميعًا يحبونك ويحتاجون إليك. لابد أن تثق بنفسك".
في أحد المرات كان يختبر فعالية تلك الطريقة، فكان يجعل المرضى يستمعون لعبارة "عندما ترى قطعة من الورق لابد أن تلتقطها"، بعد ذلك وجد أن عدد كبير من هؤلاء المرضى عندما كانوا يرون قطعة من الورق على الأرض كانوا يلتقطونها على الفور بتلقائية.
</p>
<p  dir="rtl" lang="ar" style="text-indent:30px;">علم جهاز (CIA) ما يفعله Cameron) فتواصلوا معه وأمدوه بالمال ليقوم بالتجارب والأبحاث، لكن في النهاية توصل جهاز (CIA) إلى فشل تلك الطريقة بعد ما يقرب من عشر سنوات من العمل عليها، ورفع المرضى السابقون ل(Cameron) القضايا على جهاز (CIA) لدعمه له، وتقاضوا تعويضًا من المال لم يُكشف عنه.</p>

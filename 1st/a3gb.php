<h3 dir="rtl" lang="ar" style="padding-right:20px;">قياس وزن الروح</h3>
<h4 dir="rtl" lang="ar" style="padding-right:20px;">
 (1866 م – 1920 م)Dr. Duncan MacDougall
</h4>
<p  dir="rtl" lang="ar" style="text-indent:30px;">
هو طبيب آمن بأن للروح وزن، لذلك قرر أن يجري تجارب للتحقق من صحة اعتقاده. وكانت تجاربه تقوم على قياس وزن المرضى أثناء الاحتضار وبعد الوفاة، ومن ثم الفارق في الوزن يُفترض أنه وزن الروح. أجرى تجاربه على ستة أشخاص وتوصل في النهاية إلى أن وزن الروح هو واحد وعشرون جرامًا. بعد ذلك أجرى تجاربه على خمسة عشر كلبًا، لكن النتائج كانت سلبية ولم يجد فارق في الوزن، لذلك اعتقد أن الكلاب لا تمتلك أرواح. 
</p>
<p  dir="rtl" lang="ar" style="text-indent:30px;">فيما بعد أثبت العلماء خطأ هذه النتائج لعدة أسباب منها: بساطة الأدوات المستخدمة في ذلك الوقت، عدم القدرة على التحكم الكافي في عناصر التجربة، قلة العينة. وقام بعض العلماء بتفسير ما حدث في تلك التجارب أن الأنسان ترتفع درجة حرارته بشكل مُفاجئ عند الاحتضار لتوقف الرئة عن تبريد الجسد، فيؤدي إلى ازدياد كمية العرقة المُفرزة ويفقد الجسد مزيد من السوائل، مما قد يتفق مع نتائج التجربة في حالة البشر. والعكس في حالة الكلاب فهي لا تمتلك غدد عرقية، لذلك كانت النتائج حينها سلبية.</p>

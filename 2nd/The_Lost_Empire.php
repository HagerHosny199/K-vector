<h1>The Lost Empire</h1><br>
    <div>
	<img src="TLE1.png"><br><br>
	<i>“Through violent earthquakes and floods, in a single day and night of misfortune … [the whole race] … was swallowed up by the Earth and the island of Atlantis … disappeared into the depths of the sea.”</i><br> – Plato-Greek Philsopher-427-347 B.C.<br><br>

Those were the words of Plato that opened the gates to a dream so called “Looking for Atlantis”. Over the centuries, scientists, archeologists, anthropologists, and even enthusiasts have been searching for any evidences that may prove the story of Atlantis. However, Atlantis is still a myth that everyone heard of it wants it become a truth. So if you haven’t heard of it before, give me your all ears to take a look on that mysterious empire.<br><br>
Let’s rewind the tape, all we have known about Atlantis is from the Greek Philosopher Plato’s “Timaeus and Critias dialogues” which are the only existing and specific writings talked about Atlantis. Here we come to the origin of the story, the ‘’Timaeus and Critias dialogues” are conversations between Socrates, Hermocrates, Timaeus, and Critias (Plato’s great grandfather) about the ideal societies. In these conversations, Critias and Timaeus told Socrates they would tell a story “not fiction but a true story”. The story was about the conflict between the ancient Athenians and the Atlantians 9000 years (before Plato’s time) and, here, it was the first time Atlantis was mentioned all over the time, but where do you think this story about that conflict came from?! Let me surprise you, it came from Egypt. This story is conveyed to Critias from his distant ancestor “Solon”, when he was in his first voyage to Egypt, Solon was told about Atlantis by the Egyptian priests.<br><br>
Now, it’s time to get a real look on Atlantis, as you see, Atlantis is located in the Atlantic Ocean, and divided to concentric zones of land and water. Two zones of land and three zones of water surrounded the central city, and the Island is surrounded by mountains on three sides. The area of Atlantis was estimated by 230 x 340 miles (according to Plato’s writings).<br><br>
    <img src="TLE2.png"><br><br>
	Let’s see Atlantis from inside. Atlantis had advanced technologies that are beyond even what we have reached today, it had unlimited resources, and was heavily populated. Atlantis was full of large fascinating places, gardens, temples, and a network of bridges and canals that connected all the island. The buildings were built from white, black, and red stones.<br><br>
Maybe you are wondering; how this great island have disappeared?! That will lead us to a very critical part in the story of Atlantis, it’s the mythical part.<br><br>
According to Plato’s writings; Atlantis was under authority of Poseidon (god of sea). Although people of Atlantis were very unique and advanced, they had become corrupted, greedy, and evil. Poseidon decided to punish Atlantis by flooding it, then in a single day and night, Atlantis has disappeared!<br><br>
	<img src="TLE3.png">
	<h3 style="text-align: center;">Poseidon</h3><br>
	This part is very confusing, but there are people who looked to the whole story differently. Most of people who heard about that story, saw it as a metaphor, imaginary story where Plato discussed the impact between people and gods showing how highly advanced and civilized people had turned into evil. Some others agree with the general idea of a highly advanced and civilized city that had vanished in the past, and over the centuries, people have been developing the story, especially in ancient Greece where culture of diversity of gods were the common in this time.<br><br>
So we have to get to some conclusions; Atlantis was story that Plato talked about which could be true or not; No one has reached to Atlantis yet; If Atlantis is just a myth, we may have some fun with the story; If Atlantis is a truth, one day, we will be about to see great discovery that will change all the world forever.<br><br>
